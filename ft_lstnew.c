#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list *new_link;

	if ((new_link = (t_list *)malloc(sizeof(t_list))) == NULL)
		return (NULL);
	if (content == NULL)
	{
		new_link->content = NULL;
		new_link->content_size = 0;
	}
	else
	{
		if ((new_link->content = ft_memalloc(content_size)) == NULL)
		{
			ft_memdel((void **)new_link);
			return (NULL);
		}
		ft_memcpy(new_link->content, content, content_size);
		new_link->content_size = content_size;
	}
	new_link->next = NULL;
	return (new_link);
}
